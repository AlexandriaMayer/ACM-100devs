const kelvin = 9001
// temperature in kelvin stored as int
let celsius = kelvin - 273
// celsius is 273 degrees less than kelvin
let fahrenhiet = Math.floor(celsius * (9/5) + 32)
// convertign from celsius to fahrenhiet, rounding the number
let newton = Math.floor(celsius * (33/100));
console.log(`The temperature is ${fahrenhiet} degrees Fahrenheit`)
console.log(`It is ${newton} Newtons outside`)

